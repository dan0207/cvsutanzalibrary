                                    <li>
                                        <h5>Theses Section</h5>
                                        <p>Housed in this section are manuscripts of theses and dissertations submitted by CVSU students, faculty, and employees appropriate for the research needs of the University. It offers the following services:</p>
                                        <ol type="a">
                                            <li>Charging and discharging of theses or manuscripts;</li>
                                            <li>Provides reference assistance to library users;</li>
                                            <li>Stamping of mark ownership on manuscripts (theses and dissertations) received, cataloging, classifying, accessioning, labeling, barcoding, and shelving;</li>
                                            <li>Maintenance of record theses received;</li>
                                            <li>Consolidates of abstracts of theses and dissertations submitted;</li>
                                            <li>Consolidating of statistics of users and materials usage (daily, weekly, monthly);</li>
                                            <li>Maintenance of stacks area (shelving and shelfreading); and</li>
                                            <li>Repair mutilated theses and dissertations.</li>
                                        </ol>
                                    </li>
                                    <br>
                                    <li>
                                        <h5>Periodicals Section</h5>
                                        <p>This section consists of periodical collections of foreign and local journals/magazines and newspapers acquired by the University. It offers the following services:</p>
                                        <ol type="a">
                                            <li>Charging and discharging of books;</li>
                                            <li>Provides reference assistance to library users;</li>
                                            <li>Newspaper Clippings. Clippings are maintained to provide researchers with easy access in locating information from newspaper sources;</li>
                                            <li>Indexing. A periodical index is a type of reference source that lists periodical articles by subject or author. If one has a topic in mind, a periodical index can help him/her find articles about that topic. An index will point to the right periodical, the specific date or issue copy, and even the pages for a specific article. A periodical index works like a subject catalog for the articles within a group of magazines and journals. The process of using an index is similar to the process of doing subject or keyword searches for items in a library catalog. The University Library has both print indexes in book form and computer-based indexes available in Excel;</li>
                                            <li>Selected articles of periodicals are organized and stored in a vertical file;</li>
                                            <li>Maintenance of record serials subscribed and received;</li>
                                            <li>Consolidating of statistics of users and materials usage (daily, weekly, monthly);</li>
                                            <li>Maintenance of stacks area (shelving and shelfreading); and</li>
                                            <li>Repair mutilated serials.</li>
                                        </ol>
                                    </li>
