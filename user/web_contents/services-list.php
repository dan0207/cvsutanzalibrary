                                            <li>Encoding of bibliographic records of newly processed manuscripts (theses and dissertations);</li>
                                            <li>Assisting and monitoring the students entering the Wi-Fi lounge; and</li>
                                            <li>Assisting and monitoring the students using the Collaboration Room and Learning Commons.</li>
